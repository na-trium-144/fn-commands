require("fn-commands")
BPMStatic(1, 170)
AccelStatic(2, 170)
BeatStatic(3, { { 4, 4, 4, 4 } })
StepStatic(4, 16, 4)
function a(big)
  for i = 0, 2 do
    Note(-3 + i, 1, 3, big)
    StepStatic(8, 3, 8)
    Note(-3 + i, 1, 3, false)
    StepStatic(10, 3, 8)
    Note(-3 + i, 1, 3, false)
    StepStatic(12, 2, 8)
  end
  Note(-3 + 3, 1, 3, big)
  StepStatic(15, 3, 8)
  Note(-3 + 3, 1, 3, false)
  StepStatic(17, 2, 8)
  Note(-3 + 3 + 2, 1, 3, false)
  StepStatic(19, 3, 8)
end

function a_left(big)
  for i = 0, 2 do
    Note(3 - i, -1, 3, big)
    StepStatic(25, 3, 8)
    Note(3 - i, -1, 3, false)
    StepStatic(27, 3, 8)
    Note(3 - i, -1, 3, false)
    StepStatic(29, 2, 8)
  end
  Note(3 - 3, -1, 3, big)
  StepStatic(32, 3, 8)
  Note(3 - 3, -1, 3, false)
  StepStatic(34, 2, 8)
  Note(3 - 3 - 2, -1, 3, false)
  StepStatic(36, 3, 8)
end

